Vector3 utils__int_to_vector3(int x, int y, int z) {
    Vector3 new_vector = {.x = (float)x, .y = (float)y, .z = (float)z};

    return new_vector;
}

Vector3 utils__float_to_vector3(float x, float y, float z) {
    Vector3 new_vector = {.x = x, .y = y, .z = z};

    return new_vector;
}

void utils__add_vector(Vector3 *destination, Vector3 *source) {
    destination->x += source->x;
    destination->y += source->y;
    destination->z += source->z;
}